

using System;

class Program {
    static void Main() {

        // Math Methods
        Console.WriteLine(Math.Abs(40))
        Console.WriteLine(Math.Abs(40, 2))
        Console.WriteLine(Math.Sqrt(40))
        Console.WriteLine(Math.Max(40, 69)) // Returns Largest Number
        Console.WriteLine(Math.Min(40, 69)) // Returns Smallest Number
        Console.WriteLine(Math.Round(4.69)) //Rounding Numbers

        Console.WriteLine("Addition " + 4 + 7);
        Console.WriteLine("Subtraction " + (4 - 7));
        Console.WriteLine("Multiplication " + 4 * 7);
        Console.WriteLine("Division " + 4 / 7);
        Console.WriteLine("Modules " + 40 % 7);
    }
}
